# With list
numbers = [10, 20, 30, 40, 50]
length_of_list = len(numbers)

# With dictionary
person = {"name": "Alice", "age": 30, "city": "Wonderland"}
length_of_dict = len(person)

# With string
text = "Hello, world!"
length_of_string = len(text)

print("Length of list:", length_of_list)  # Output: 5
print("Length of dictionary:", length_of_dict)  # Output: 3
print("Length of string:", length_of_string)  # Output: 13
