# Conditional structure in Python

# If statement: checks if the condition is true (1 evaluates to True)
if 1:
    print("Here is 1")
# Elif statement: checks if the previous condition was false and this condition is true (0 evaluates to False)
elif 0:
    print("Here is 2")
# Else statement: executes if all previous conditions are false
else:
    print("Here is 3")
