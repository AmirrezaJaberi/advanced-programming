# Defining a tuple of fruits
fruits = ("apple", "banana", "cherry", "date")

# Accessing elements of a tuple
print(fruits[0])  # Output: apple
print(fruits[1])  # Output: banana

# Iterating through a tuple
for fruit in fruits:
    print(fruit)

