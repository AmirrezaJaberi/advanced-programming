# Example of converting range to a list
numbers_list = list(range(1, 10, 2))
print(numbers_list)

# range with start, stop, and step
for i in range(1, 10, 2):
    print(i)
# Output: 1, 3, 5, 7, 9

# range with start and stop
for i in range(2, 7):
    print(i)
# Output: 2, 3, 4, 5, 6

# range with stop
for i in range(5):
    print(i)
# Output: 0, 1, 2, 3, 4
